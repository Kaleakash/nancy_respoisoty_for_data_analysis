import numpy as np;
np_data = np.array([1,2,3,4,5,6]);
print("Square ",np.square(np_data))
print("Square root",np.sqrt(np_data))
print("Exponential",np.exp(np_data))
print("mean",np.mean(np_data))
print("median",np.median(np_data))
print("min",np.min(np_data))
print("max",np.max(np_data))
print("std",np.std(np_data))


